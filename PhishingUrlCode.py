#lexical features

import math
from collections import Counter

def lexical_features(url):
  length = len(url)
  special_chars = sum(not c.isalnum() for c in url)
  probability = [v/length for v in Counter(url).values()]
  entropy = -sum(p*math.log2(p) for p in probability if p > 0)
  return length,special_chars,entropy

