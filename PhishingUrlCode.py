class GAT_MLP(torch.nn.Module):
    def __init__(self, in_channels, hidden_channels, out_channels, heads=4, gat_conv_dropout=0.25, node_feature_dropout=0.3, mlp_dropout=0.3):
        super().__init__()

        # Store dropout rates
        self.node_feature_dropout = node_feature_dropout

        # First GAT Layer
        self.gat1 = GATConv(
            in_channels,
            hidden_channels,
            heads=heads,
            dropout=gat_conv_dropout # Dropout for attention coefficients
        )

        self.bn1 = BatchNorm1d(hidden_channels * heads)

        # Second GAT Layer
        self.gat2 = GATConv(
            hidden_channels * heads,
            hidden_channels,
            heads=1,
            concat=False,
            dropout=gat_conv_dropout # Dropout for attention coefficients
        )

        self.bn2 = BatchNorm1d(hidden_channels)

        # MLP classifier
        self.mlp = torch.nn.Sequential(
            Linear(hidden_channels, 128),
            torch.nn.ReLU(),
            Dropout(mlp_dropout), # Dropout within MLP
            Linear(128, out_channels)
        )

    def forward(self, data):
        x, edge_index = data.x, data.edge_index

        # GAT Layer 1
        x = self.gat1(x, edge_index)
        x = self.bn1(x)
        x = F.elu(x)
        x = F.dropout(x, p=self.node_feature_dropout, training=self.training) # Node feature dropout

        # GAT Layer 2
        x = self.gat2(x, edge_index)
        x = self.bn2(x)
        x = F.elu(x)

        # MLP
        x = self.mlp(x)

        return x

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

model = GAT_MLP(
    in_channels=data.x.shape[1],
    hidden_channels=64,
    out_channels=2
).to(device)

data = data.to(device)

optimizer = torch.optim.AdamW(
    model.parameters(),
    lr=0.0004,
    weight_decay=1e-5
)

criterion = torch.nn.CrossEntropyLoss()

scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(
    optimizer,
    mode='min',
    patience=10,
    factor=0.5
)

def train():
    model.train()
    optimizer.zero_grad()

    out = model(data)

    # Training loss
    train_loss = criterion(
        out[data.train_mask],
        data.y[data.train_mask]
    )

    train_loss.backward()
    torch.nn.utils.clip_grad_norm_(model.parameters(), 2.0)
    optimizer.step()

    # Calculate training accuracy
    preds = out[data.train_mask].argmax(dim=1)
    correct = (preds == data.y[data.train_mask]).sum().item()
    total = data.train_mask.sum().item()
    train_acc = correct / total

    return train_loss.item(), train_acc

def validate():
    model.eval()
    with torch.no_grad():
        out = model(data)

        val_loss = criterion(
            out[data.val_mask],
            data.y[data.val_mask]
        )

        preds = out[data.val_mask].argmax(dim=1)
        correct = (preds == data.y[data.val_mask]).sum().item()
        total = data.val_mask.sum().item()
        val_acc = correct / total

    return val_loss.item(), val_acc

for epoch in range(1, 301):

    train_loss, train_acc = train()
    val_loss, val_acc = validate()

    scheduler.step(val_loss)

    if epoch % 10 == 0:
        print(f"Epoch {epoch:03d} | "
              f"Train Loss: {train_loss:.4f} | "
              f"Train Acc: {train_acc:.4f} | "
              f"Val Loss: {val_loss:.4f} | "
              f"Val Acc: {val_acc:.4f}")

def test():
    model.eval()
    with torch.no_grad():
        out = model(data)

        # Predictions for test set
        preds = out[data.test_mask].argmax(dim=1)

        y_true = data.y[data.test_mask]
        y_pred = preds

        # Accuracy
        correct = (y_pred == y_true).sum().item()
        total = data.test_mask.sum().item()
        acc = correct / total

    return acc, y_true.cpu(), y_pred.cpu()

acc, y_true, y_pred = test()

print("Final Accuracy:", acc)
