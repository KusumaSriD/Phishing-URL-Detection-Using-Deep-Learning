"""**Dynamic Feature Fusion**"""

import torch
import torch.nn as nn
import torch.nn.functional as F

class DynamicFeatureFusion(nn.Module):
    def __init__(self, lex_dim, struct_dim, stat_dim):
        super(DynamicFeatureFusion, self).__init__() #calls parent constructor

        # Learnable attention weights
        self.w_lex = nn.Parameter(torch.randn(lex_dim))
        self.w_struct = nn.Parameter(torch.randn(struct_dim))
        self.w_stat = nn.Parameter(torch.randn(stat_dim))

    def forward(self, lex, struct, stat):
        # Apply attention (element-wise multiplication)
        lex_att = lex * self.w_lex
        struct_att = struct * self.w_struct
        stat_att = stat * self.w_stat

        # Concatenate weighted features
        fused = torch.cat([lex_att, struct_att, stat_att], dim=1)
        return fused
